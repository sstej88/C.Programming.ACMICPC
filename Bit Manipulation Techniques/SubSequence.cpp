vector<string> solve() {
    vector<string> r;

    string str;
    cin>>str;
    int n = str.size();
    
    for(int i=0; i<pow(2, n); i++) {
        int bck = i;
        int pos = 0;
        string sub = "";
        while(bck) {
            if(bck&1) sub+=str.at(pos);
            bck>>=1;
            pos++;
        }
        r.push_back(sub);
    }

    return r;
}

