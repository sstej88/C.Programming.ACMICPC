vector<int> countBits(int n) {
    vector<int> r;
    
    if(n>=2) {
        map<int, int> dp;
        dp[0] = 0;
        dp[1] = 1;

        r.push_back(0);
        r.push_back(1);

        int index = 0;
        for(int i=2; i<=n; i++) {
            if((i&(i-1))==0) {
                index = 0;
            }
            dp[i] = 1+dp[index];
            r.push_back(dp[i]);
            index++;
        }
    }
    else if(n==1) {
        r.push_back(0);
        r.push_back(1);
    }
    else {
        r.push_back(0);
    }
    

    return r;
}

